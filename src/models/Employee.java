package models;

public class Employee {
    public String fullName;
    public String gender;
    public int age;
    public float salary;
    public String department;

    public Employee(String fullName,
                    String gender,
                    int age,
                    float salary,
                    String department) {
        this.fullName = fullName;
        this.gender = gender;
        this.age = age;
        this.salary = salary;
        this.department = department;
    }
}







